package Beans;

/**
 *
 * @author luis
 */
public class Album {
    private int id;
    private String descricao;

    public Album() {
    }

    public Album(int id, String descricao) {
        this.id = id;
        this.descricao = descricao;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
